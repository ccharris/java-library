package _03_numbers.java;

public class _04_challenge_story {

	public static void main(String[] args) {
		
		int numberCats = 2;
		// Our story starts with our main character Finn who owns 2 cats.
		// Finn's boyfriend Poe moves in and brings his 3 cats.
		numberCats += 3;
		//now they have this many cats.
		System.out.println("Poe and Finn have " + numberCats + " cats.");
				
		// Poe and Finn ran into the cutest abandon kitten and just had to take her home
		numberCats += 1;
		System.out.println("Poe and Finn have " + numberCats + " cats.");
		
		//Then they decide to split up, and will split the number of cats evenly.
		numberCats = numberCats / 2;
		System.out.println("Poe and Finn now each have " + numberCats + " cats.");

		
		
		
		
		
	}

}
