package _05_Booleans;

public class _01_booleans {
	public static void main(String[] args) {
	String homeScreen = "locked";
		if (homeScreen.equals("locked")) {
			System.out.println("The homescreen is locked");
		}
	boolean isTired = true;
		if (isTired) {
			System.out.println("Yes I am tired");
		}
	boolean homeScreenLock = true;
		if (homeScreenLock) {
			System.out.println("The screen is definitely locked");
		}
	boolean isOn = false;
		if (isOn) {
			System.out.print("The thing is on!!");
		} else {
			System.out.print("The thing is not on!!");
		}
	
}
}